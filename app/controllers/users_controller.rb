class UsersController < ApplicationController
  before_action :set_user, only: %i[show update destroy]

  def collect_name
    response = ::Users::CollectNameService.new.execute
    if response.error
      render json: { message: response.error }, status: :unprocessable_entity
    else
      render json: { names: response.result }
    end

  end

  # GET /users
  def index
    page = params[:page].to_i.positive? ? params[:page].to_i : 1
    per_page = params[:per_page].to_i.positive? ? params[:per_page].to_i : 1
    @users = User.paginate(page: page, per_page: per_page)

    render json: { users: @users, meta: meta_attributes(@tenants) }
  end

  # GET /users/1
  def show
    render json: @user
  end

  # POST /users
  def create
    @user = User.new(user_params)

    if @user.save
      render json: @user, status: :created, location: @user
    else
      render json: @user.errors, status: :unprocessable_entity
    end
  end

  # PATCH/PUT /users/1
  def update
    if @user.update(user_params)
      render json: @user
    else
      render json: @user.errors, status: :unprocessable_entity
    end
  end

  # DELETE /users/1
  def destroy
    @user.destroy
  end

  private

  # Use callbacks to share common setup or constraints between actions.
  def set_user
    @user = User.find(params[:id])
  end

  # Only allow a trusted parameter "white list" through.
  def user_params
    params.require(:user).permit(:email, :password)
  end
end
